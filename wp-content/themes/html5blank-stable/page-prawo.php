													<li class="unit-one">4. Zgoda rodzica dziecka oznacza zgodę co najmniej jednego z rodziców dziecka. Jednak w przypadku braku porozumienia między rodzicami dziecka należy poinformować rodziców o konieczności rozstrzygnięcia sprawy przez sąd rodzinny.</li>
													<li class="unit-one">5. Przez krzywdzenie dziecka należy rozumieć popełnienie czynu zabronionego lub czynu karalnego na szkodę dziecka przez jakąkolwiek osobę, w tym pracownika placówki, lub zagrożenie dobra dziecka, w tym jego zaniedbywanie.</li>
													<li class="unit-one">6. Osoba odpowiedzialna za Politykę ochrony dzieci przed krzywdzeniem to wyznaczony przez pastora Zboru pracownik/wolontariusz sprawujący nadzór nad realizacją Polityki ochrony dzieci przed krzywdzeniem w placówce.</li>

													<li class="unit-one">7. Dane osobowe dziecka to wszelkie informacje umożliwiające identyfikację dziecka.</li>
