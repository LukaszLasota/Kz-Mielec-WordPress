												<ul>
													<li class="unit-one">§ 1.</li>
													<li class="unit-one">1. Pracownikiem i wolontariuszem Służby Katechetycznej jest osoba prowadząca zajęcia, spotkania z dziećmi i młodzieżą oraz sprawująca opiekę nad nimi.</li>
													<li class="unit-one">2. Dzieckiem jest każda osoba do ukończenia 18. roku życia.</li>
