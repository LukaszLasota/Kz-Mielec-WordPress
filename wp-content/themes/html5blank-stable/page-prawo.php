													<li class="unit-one">W przypadku podjęcia przez pracownika lub wolontariusza Służby Katechetycznej podejrzenia, że dziecko jest krzywdzone, pracownik lub wolontariusz ma obowiązek sporządzenia notatki służbowej i przekazania uzyskanej informacji (do wyboru) pastorowi Zboru.</li>
													<li class="unit-one">§ 4.</li>
													<li class="unit-one">1. Pracownik lub wolontariusz Służby Katechetycznej wzywa opiekunów dziecka, którego krzywdzenie podejrzewa, oraz informuje ich o podejrzeniu.</li>
													<li class="unit-one">2. Pracownik lub wolontariusz Służby Katechetycznej sporządza opis sytuacji dziecka na podstawie rozmów z dzieckiem i rodzicami oraz plan pomocy dziecku.</li>
