					<section class="collapse collapse--border">
					<div class="collapse__head"><div class="collapse__text">HISTORIA KOŚCIOŁA ZIELONOŚWIĄTKOWEGO</div></div>
					  <div class="collapse__body">
					  <div class="sub-text">
					
				
				<p class="sub-text__paragraph space">
					Istnieje brak jednolitego stanowiska wśród historyków zielonoświątkowych na temat: gdzie, kiedy i pod czyim przewodnictwem 
					powstał ruch zielonoświątkowy. Dyskusja dotyczy zasadniczo dwóch dat i dwóch różnych miejsc w Stanach Zjednoczonych: 
					roku 1901 - Topeka w stanie Kansas oraz roku 1906 - Los Angeles. 
				</p>	

				<div class="center">
				<figure>
					<img src="<?php echo get_template_directory_uri(); ?>/img/historia/open01.jpg" alt="Azusa Street" />
					
					<figcaption>Azusa Street</figcaption>	
				</figure>
				</div>

				<p class="sub-text__paragraph space">	
					W. Hollenweger, uznany badacz omawianej problematyki, 
					proponuje rozpatrywać zagadnienie początku pentekostalizmu z dwóch punków widzenia: ideengeschichte (historii idei) wówczas 
					należy wziąć pod uwagę pierwszą datę oraz realgeschichte (historię rzeczywistości) wówczas możemy mówić o drugiej dacie narodzin ruchu. 
					Od tego czasu ruch zielonoświątkowy zaczął się rozprzestrzeniać po całym świecie.
				</p>
				<div class="center">
				<figure>
					<img src="<?php echo get_template_directory_uri(); ?>/img/historia/charlesparham.png" alt="Charles Fox Parham" />
					
					<figcaption>Charles Fox Parham</figcaption>	
				</figure>
				</div>
				<p class="sub-text__paragraph space">
					Część historyków uważa, że początki ruchu związane są z osobą Charlesa Foxa Parhama. Założył on w roku 1900 w Topeka Szkołę Biblijną. 
					W noc sylwestrową 1 stycznia 1901 roku jedna ze "studentek" tej szkoły, Agnes Ozman, w czasie wspólnej modlitwy doświadczyła daru mówienia 
					innymi językami. Wkrótce i inni członkowie Bethel Biblie College zostali napełnieni Duchem Świętym. W 1906 r. Parham otworzył w Houston drugą 
					szkołę biblijną, gdzie również dochodziło do doświadczenia chrztu w Duchu. Wśród nich był czarnoskóry kaznodzieja Ruchu Uświęceniowego 
					- William J. Seymour. W 1906 roku, pod jego kierownictwem, rozpoczyna działalność Misja przy Azusa Street 312 w Los Angeles. 
					Misja cieszyła się tak wielkim powodzeniem, że ściągała uwagę opinii publicznej nie tylko w USA, ale również w Europie. Uzdrowienia, cuda, 
					proroctwa i mówienie w innych językach wywoływały duże poruszenie. Oprócz tych nadzwyczajnych wydarzeń, przebudzenie przy Azusa Street było 
					także procesem tworzenia się nowej wspólnoty. Przyczyną tego stanu było odrzucenie przez dotychczasowe kongregacje tych, którzy dzielili się 
					swoim doświadczeniem chrztu w Duchu Świętym. Dla tej przyczyny większość historyków właśnie rok 1906 uznaje za początek współczesnego 
					pentekostalizmu. 
				</p>
				<div class="center">
				<figure>
					<img src="<?php echo get_template_directory_uri(); ?>/img/historia/WilliamJSeymourMinister.jpg" alt="William J. Seymour">
					
					<figcaption>William J. Seymour</figcaption>	
				</figure>
				</div>
				<p class="sub-text__paragraph space">
					Jak gwałtownie rozwijał się ruch zielonoświątkowy, niech świadczy to, iż w Europie już w roku 1907 powstały pierwsze wspólnoty tego typu. 
					Jednym z "apostołów" pentekostalizmu był Thomas Bell Barratt, superintendent zborów metodystycznych w Oslo. W czasie swojej wyprawy do USA w roku 1906 przeżył 
					chrzest w Duchu Świętym i stał się orędownikiem powstałego w Los Angeles ruchu. Konflikt, w jaki popadł z władzami swojego Kościoła, zmusili ostatecznie do 
					utworzenia nowego zboru w Oslo. Podobne wypadki wydarzyły się w Szwecji, gdzie baptystyczny kaznodzieja - Levi Pethrus - został w roku 1907 zmuszony do założenia 
					samodzielnego zboru w Sztokholmie. W roku 1906 w Bydgoszczy chrzest w Duchu Świętym przeżył ks. ewangelicki Jonathan Paul jeden z najaktywniejszych charyzmatyków 
					niemieckich pierwszej połowy XX wieku. Z Norwegii, Szwecji i Niemiec ruch zielonoświątkowy rozszerza się na okoliczne kraje i wkrótce powstały 
					Kościoły zielonoświątkowe w Anglii, Finlandii, Danii i Szwajcarii.  
				</p>
				<div class="center">
				<figure>
					<img src="<?php echo get_template_directory_uri(); ?>/img/historia/barrattt.jpg" alt="Thomas Bell Barratt" />
					
					<figcaption>Thomas Bell Barratt</figcaption>	
				</figure>
				</div>
				<p class="sub-text__paragraph space">
					Na ziemie polskie pentekostalizm wkroczył z dwóch stron. Wpływy przebudzenia niemieckiego doprowadziły do zawiązania się pierwszych wspólnot wyznaniowych nowego 
					ruchu na Śląsku Cieszyńskim. Tu w roku 1910 powstał Związek Stanowczych Chrześcijan, który formalnie należał do Kościoła ewangelickiego de facto tworząc niezależną 
					wspólnotę - pierwszy polski Kościół zielonoświątkowy. Reemigranci polscy, wracając z USA do odrodzonej Ojczyzny, przynieśli wieści o chrzcie w Duchu Świętym. 
					W centralnej i wschodniej części II RP doszło do zawiązywania się autonomicznych wspólnot zielonoświątkowych. Dopiero jednak w 1929 w Starej Czołnicy koło Łucka 
					odbył się zjazd powołujący do istnienia Kościół Chrześcijan Wiary Ewangelicznej. Tak nazywać się będzie wspólnota polskich zielonoświątkowców w tej części naszego kraju. 
					W okresie międzywojennym istniały w Polsce przynajmniej dwa kościoły skupiające charyzmatyków. Duchownych kształciło się na kursach w specjalnie do tego celu otwartym 
					Instytucie Biblijnym w Gdańsku. 
				</p>	
					
				<p class="sub-text__paragraph space">	
					Okres II wojny światowej to czas delegalizacji związków kościelnych. Po wojnie duża część zielonoświątkowców na kresach wschodnich pozostała poza granicami PRL. 
					Rozpoczął się powolny proces odbudowy struktur kościelnych w nowej sytuacji społeczno-politycznej. Komuniści dążyli do zminimalizowania aktywności grup religijnych 
					w Polsce i poddania ścisłej kontroli służb bezpieczeństwa. Pod naciskiem państwa powstał Zjednoczony Kościół Ewangeliczny (1947), twór dość nietypowy, 
					bo zrzeszający nie tylko oba nurty polskiego ruchu zielonoświątkowego, ale także inne kościoły: ewangeliczne, Wolnych Chrześcijan, Kościół Chrystusowy, 
					Kościół Ewangelicznych Chrześcijan, a początkowo również baptystów. Baptyści pierwsi opuścili szeregi ZKE. Historia ZKE ściśle związana jest z dziejami narodu w tym czasie. 
				</p>
				<div class="center">
				<figure>
					<img src="<?php echo get_template_directory_uri(); ?>/img/historia/b_kchwe.jpg" alt="Kościół Chrześcijan Wiary Ewangelicznej" style="width: 400px;"/>
					
					<figcaption>Kościół Chrześcijan Wiary Ewangelicznej</figcaption>	
				</figure>
				</div>
				<p class="sub-text__paragraph space">
					Zielonoświątkowcy przestali być częścią ZKE w roku 1987. Kościół ten podjął wówczas decyzję o samorozwiązaniu i powstaniu oddzielnych jednostek konfesyjnych. 
					Większość przedstawicieli pentekostalizmu w Polsce powoła do istnienia Kościół Zielonoświątkowy. Istnieje jednak wiele wspólnot zielonoświątkowych, które nie są zrzeszone 
					w strukturach Kościoła Zielonoświątkowego. Przyczyny tego braku jedności tkwią w samym rozumieniu istoty Kościoła w środowisku wolnokościelnym, do którego należą m. in. zielonoświątkowcy. 
					Kościół rozumiany jako wartość mistyczna jest jeden i powszechny. Jednak w wymiarze organizacyjnym jest reprezentowany przez różnorodne wspólnoty, które traktują się w sposób braterski. 
					</p>
					<p class="sub-text__paragraph space">
					Pomiędzy głównymi kościołami zielonoświątkowymi w Polsce nie występują żadne poważne rozbieżności teologiczne, nie licząc kilka pomniejszych kościołów, które przejęły pewne nauki, różne 
					od głównego nurtu klasycznej teologii zielonoświątkowej. Przykładem może być Zbór Ewangelicznych Chrześcijan w Duchu Apostolskim w Ełku i Jednota Braci Polskich. Odrzucają oni wiarę 
					w Trójcę Świętą. Każda z tych formacji, wychodząc zresztą z odmiennego założenia, uznaje dogmat o jedności bóstwa rozumianego w sposób jednoosobowy, a Bracia Norwescy głoszą możliwość nie popełniania grzechów po nawróceniu. 
				</p>
				
				<p class="sub-text__paragraph space">
					Na terenie Warmii i Mazur w okresie międzywojennym istniały zbory zielonoświątkowe, skupiające ludność niemiecką. Po wojnie, w związku z emigracją tej ludności do Niemiec zbory 
					te przestały istnieć. Nowi mieszkańcy północnej Polski przynieśli ruch zielonoświątkowy w szacie polskojęzycznej. Pierwsze zbory występujące pod nazwą Kościoła Chrześcijan 
					Wiary Ewangelicznej powstały w Szczytnie, Kętrzynie, Ostródzie a później także w Elblągu i Olsztynie. 
				</p>
				
				<p class="sub-text__paragraph space">
					Doktrynalnie zielonoświątkowcy znajdują się wśród wspólnot protestanckich o zabarwieniu baptystycznym. Podstawą doktrynalną Kościoła Zielonoświątkowego jest wiara w osobistą 
					potrzebę nawrócenia. W wyniku nawrócenia i wiary w zbawczą śmierć na krzyżu Jezusa Chrystusa powstaje potrzeba chrztu, który sprawowany jest poprzez zanurzenie katechumena w wodzie 
					w imię Ojca i Syna i Ducha Świętego. Kościół zielonoświątkowy uznaje orzeczenia czterech pierwszych Soborów Powszechnych, dlatego w pełni akceptuje Skład Apostolski oraz 
					nicejsko-konstantynopolitańskie wyznanie wiary. Zielonoświątkowcy w Polsce posiadają również swoje własne wyznanie wiary, które z jednej strony nawiązuje do podobnych tego typu 
					w kościołach pentekostalnych na świecie, z drugiej zaś jest tworem oryginalnie polskim.
				</p>
				
				
