					<p class="sub-text__paragraph space">Historia rozpoczyna się gdy w roku 1993 w Mielcu zostaje powołana Stacja Misyjna Kościoła. Kierownikiem zostaje prezb. Andrzej Brandys.</p>
					<p class="sub-text__paragraph space">Zostaje zorganizowana ewangelizacja, do Mielca przyjeżdża Misja Namiotowa. Przez pięć dni zwiastowana była Ewangelia, odbywały się koncerty muzyki chrześcijańskiej. Był to również czas zwiastowania Słowa Bożego (kaznodzieją był obecny prezb. Leszek Mocha).</p>
					<p class="sub-text__paragraph space">W wyniku zwiastowanej Dobrej Nowiny rozpoczęły się spotkania domowe. Kolejnym etapem były spotkania w Osiedlowym Domu Kultury, na które przyjeżdżali okoliczni pastorzy, by usługiwać.</p>
					<p class="sub-text__paragraph space">Nadszedł czas, by z nieformalnej grupy powstał Zbór, który będzie miał swojego pastora. W roku 1994 (8 marca) został powołany do istnienia Zbór Kościoła Zielonoświątkowego w Mielcu. Naczelna Rada Kościoła zatroszczyła się również o pasterza. Pierwszym pastorem został br. Marek Skrzypczak (od 1994 do 2000). Zbór zaczął żyć; pierwszy ślub, rozpoczęcie katechetyzacji, praca z młodzieżą. Kościół wynajął salę przy ul. Reja 1, która od tego czasu zaczęła służyć jako kaplica.</p>
					<p class="sub-text__paragraph space">W roku 2000 pastor Marek Skrzypczak przeniósł się do Rzeszowa, by tam objąć urzad pastora. Natomiast w Mielcu służbę objął br. Janusz Kaleta. W tym czasie zbór zaangażował się pracę charytatywną (wydawanie odzieży używanej i produktów żywnościowych), by w sposób praktyczny ukazywać miłość Boga do człowieka. W 2007 roku pastor Janusz Kaleta wyjechał do służby pastorskiej w Edmonton (Kanada).</p>
					<p class="sub-text__paragraph space">Zbór był przygotowany do wyjazdu pastora i przez okres ok. roku pozostając pod opieką pastora Zboru rzeszowskiego (prezb. Marek Skrzypczak) prowadził pracę na miarę sił i możliwości.</p>
					<p class="sub-text__paragraph space">W roku 2008 (koniec września) do Mielca przyjechał diakon Dariusz Hapoń wraz z małżonką Małgorzatą. Do tej pory (ordynowany w 2002r) służył w Zborze "Pojednanie" KZ w Międzyrzecu Podlaskim jako kaznodzieja i starszy Zboru. Powołany na pastora Zboru mieleckiego 19 października 2011.</p>
